#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	return ((*s1 == *s2 && *s1) ? ft_strcmp(s1 + 1, s2 + 1)
			: (unsigned char)*s1 - (unsigned char)*s2);
}
